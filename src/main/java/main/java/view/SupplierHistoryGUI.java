    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
     String keyword =
        txtSearchSupplierID.getText();

try {

    Connection conn =
            new SupplierDB().connectDB();

    String sql =
"SELECT * FROM supplier_history WHERE supplier_name LIKE ?";

    PreparedStatement pst =
            conn.prepareStatement(sql);

    pst.setString(1,
            "%" + keyword + "%");

    ResultSet rs =
            pst.executeQuery();

    DefaultTableModel model =
            (DefaultTableModel)
            tblSupplierHistory.getModel();

    model.setRowCount(0);

    while(rs.next()) {

        model.addRow(new Object[]{


rs.getString("supplier_name"),
rs.getString("book_title"),
rs.getInt("quantity"),
rs.getString("action_description"),
rs.getString("action_date")


});

    }

    conn.close();

} catch(Exception e) {

    e.printStackTrace();
} 
    }//GEN-LAST:event_btnSearchActionPerformed

