    public void loadPurchaseOrders() {

    try {

        Connection conn =
                new SupplierDB().connectDB();

        String sql =
                "SELECT * FROM purchase_orders";

        PreparedStatement pst =
                conn.prepareStatement(sql);

        ResultSet rs =
                pst.executeQuery();

        DefaultTableModel model =
                (DefaultTableModel)
                tblPurchaseOrder.getModel();

        model.setRowCount(0);

        while(rs.next()) {

            model.addRow(new Object[]{

                rs.getInt("purchase_order_id"),
                rs.getString("order_date"),
                rs.getDouble("total_amount")
            });
        }

        conn.close();

    } catch(Exception e) {

        e.printStackTrace();
    }
}
