    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
       try {

    Connection conn =
            new SupplierDB().connectDB();

    String sql =
"INSERT INTO purchase_orders(order_date, total_amount) VALUES (?, ?)";

    PreparedStatement pst =
            conn.prepareStatement(sql);

    pst.setString(1,
            txtOrderDate.getText());

    pst.setDouble(2,
            Double.parseDouble(
                    txtTotalAmount.getText()
            ));

    pst.executeUpdate();
    
    String historySql =
"INSERT INTO supplier_history " +
"(supplier_name, action_description, action_date, book_title, quantity) " +
"VALUES (?, ?, NOW(), ?, ?)";

PreparedStatement historyPst =
conn.prepareStatement(historySql);

historyPst.setString(
1,
"Tech Supplier"
);

historyPst.setString(
2,
"Books Supplied"
);

historyPst.setString(
3,
txtBookTitle.getText()
);

historyPst.setInt(
4,
Integer.parseInt(
txtQuantity.getText()
)
);

historyPst.executeUpdate();

    JOptionPane.showMessageDialog(
            this,
            "Purchase Order Saved!"
    );

    conn.close();

    loadPurchaseOrders();

} catch(Exception e) {

    e.printStackTrace();
}
    }//GEN-LAST:event_btnSaveActionPerformed

